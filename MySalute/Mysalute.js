$(document).ready(function(){
$("#container1").show();
$("#container2").hide();
$("#container3").hide();   
$("#btnnav1").click(function(){
	$("#container1").show();
	$("#container2").hide();
	$("#container3").hide();
});
$("#btnnav2").click(function(){
	$("#container1").hide();
	$("#container2").show();
	$("#container3").hide();
});
$("#btnnav3").click(function(){
	$("#container1").hide();
	$("#container2").hide();
	$("#container3").show();
});
});